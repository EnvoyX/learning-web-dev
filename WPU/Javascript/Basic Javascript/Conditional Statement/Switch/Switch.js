var valueNumber = parseInt(prompt('Please input a number between 1-3'));

switch (valueNumber) {
  case 1:
    alert('Anda memasukan angka 1');
    console.log('Anda memasukan angka 1');
    break;
  case 2:
    alert('Anda memasukan angka 2');
    console.log('Anda memasukan angka 2');
    break;
  case 3:
    alert('Anda memasukan angka 3');
    console.log('Anda memasukan angka 3');
    break;
  default:
    console.log('Anda memasukan angka yang salah');

    break;
}
